int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (i == 1)
		return (1);
	while (i * i <= nb && i <= 46340)
	{
		if (i * i == nb)
			return (i);
		++i;
	}
	return (0);
}
