#include <fcntl.h>
#include <stdio.h>
#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		fd;
	char	c;

	if (argc < 2)
	{
		write(2, "File name missing.\n", 19);
		return (1);
	}
	if (argc > 2)
	{
		write(2, "Too many arguments.\n", 20);
		return (2);
	}
	fd = open(argv[1], O_RDONLY);
	if (fd == -1)
	{
		write(2, "Cannot read file.\n", 18);
		return (2);
	}
	while (read(fd, &c, 1))
		write(1, &c, 1);
	close(fd);
	return (0);
}
